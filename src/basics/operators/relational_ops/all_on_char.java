package basics.operators.relational_ops;

public class all_on_char {
    public static void main(String args[]) {
        char x = 'A';
        char y = 'B';

        System.out.println("x == y: "+(x==y));
        System.out.println("x != y: "+(x!=y));
        System.out.println("x >= y: "+(x>=y));
        System.out.println("x <= y: "+(x<=y));
        System.out.println("x > y: "+(x>y));
        System.out.println("x < y: "+(x<y));
    }
}
