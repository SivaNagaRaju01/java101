package basics.data_types;

public class char_type {
    public static void main(String args[]){
        char x = '2';
        char y = '5';
        
        System.out.println("x, y = "+x+", "+y);
        x++;
        y++;
        System.out.println("x++, y++ = "+x+", "+y);
        
        System.out.println("x+y = "+(x+y));
    }
}
